export function BottomNav() {
  const pathname = usePathname();
  const { address } = useAccount();
  const [isAdmin, setIsAdmin] = useState(false);

  // Check if user is admin
  useEffect(() => {
    async function checkAdmin() {
      if (!address) {
        setIsAdmin(false);
        return;
      }
      
      try {
        const res = await fetch(`/api/farcaster/user?address=${address}`);
        if (res.ok) {
          const data = await res.json();
          setIsAdmin(data.user?.fid === ADMIN_FID);
        }
      } catch {
        setIsAdmin(false);
      }
    }
    
    checkAdmin();
  }, [address]);

  const visibleTabs = isAdmin ? [...tabs, adminTab] : tabs;

  return (
    <nav className="fixed bottom-0 left-0 right-0 z-40 bg-[#0a0a0a] border-t border-[#222222] safe-bottom">
      <div className="flex items-center justify-around h-16 max-w-[430px] mx-auto">
        {visibleTabs.map((tab) => {
          const isActive = pathname === tab.href || 
            (tab.href !== '/' && pathname.startsWith(tab.href));
          
          return (
            <Link
              key={tab.name}
              href={tab.href}
              className={cn(
                'flex flex-col items-center justify-center w-full h-full gap-1',
                'transition-colors duration-200',
                isActive ? 'text-white' : 'text-[#666666]'
              )}
            >
              {tab.icon(isActive)}
              <span className="text-[10px] font-medium">{tab.name}</span>
            </Link>
          );
        })}
      </div>
    </nav>
  );
}
